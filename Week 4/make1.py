# 2 = 1을 빼거나 2로 나누기 (1번)
# 3 = 1을 빼면 2번 / 3으로 나누면 1번 (1번)
# 4 = 1을 빼면 2번 / 2로 나누면 2번 (2번)
# 5 = 1을 빼면 3번 (3번)
# 6 = 1을 빼면 4번 / 2로 나누면 2번 / 3으로 나누면 2번 (2번) => 2로 나누고 3으로 나누기 / 3으로 나누고 2로 나누기